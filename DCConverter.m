function dz = DCConverter(t,z)
% ----------------------------------------------------
% Parameters
% ----------------------------------------------------
L = 2e-3; R=10; C=10e-6; U_in=32;
% PWM:
D = 40/100;
f = 100e3;
T = 1/f;
d_t = D*T;
d = 0.5;
% ----------------------------------------------------
% Matrix Form
% ----------------------------------------------------
A = [0 -1/L;1/C -1/R*C];
B = [d;0];

% ----------------------------------------------------
% Definition of the dynamics of the system
% ----------------------------------------------------
% State Vector: dz = [z1_d;z2_d]
dz = zeros(2,1); 

%dz = A*[z(1);z(2)]+B*U_in; 
dz(1)=-((1/L)*z(2))+((U_in/L)*d); 
dz(2)=((1/C)*z(1))-((1/(R*C))*z(2));
%-----------------------------------------------------